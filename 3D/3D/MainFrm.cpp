void MainFrame::OnViewCustomize()
{
	CMFCToolBarsCustomizeDialog* pDlgCust = new CMFCToolBarsCustomizeDialog(this, TRUE /* scan menus */);
	pDlgCust->EnableUserDefinedToolbars();
	pDlgCust->Create();
}

LRESULT MainFrame::OnToolbarCreateNew(WPARAM wp,LPARAM lp)
{
	LRESULT lres = CFrameWndEx::OnToolbarCreateNew(wp,lp);
	if (lres == 0)
	{
		return 0;
	}

	CMFCToolBar* pUserToolbar = (CMFCToolBar*)lres;
	ASSERT_VALID(pUserToolbar);

	BOOL bNameValid;
	CString strCustomize;
	bNameValid = strCustomize.LoadString(IDS_TOOLBAR_CUSTOMIZE);
	ASSERT(bNameValid);

	pUserToolbar->EnableCustomizeButton(TRUE, ID_VIEW_CUSTOMIZE, strCustomize);
	return lres;
}


BOOL MainFrame::LoadFrame(UINT nIDResource, DWORD dwDefaultStyle, CWnd* pParentWnd, CCreateContext* pContext)
{
	// base class does the real work

	if (!CFrameWndEx::LoadFrame(nIDResource, dwDefaultStyle, pParentWnd, pContext))
	{
		return FALSE;
	}


	// enable customization button for all user toolbars
	BOOL bNameValid;
	CString strCustomize;
	bNameValid = strCustomize.LoadString(IDS_TOOLBAR_CUSTOMIZE);
	ASSERT(bNameValid);

	for (int i = 0; i < iMaxUserToolbars; i ++)
	{
		CMFCToolBar* pUserToolbar = GetUserToolBarByIndex(i);
		if (pUserToolbar != nullptr)
		{
			pUserToolbar->EnableCustomizeButton(TRUE, ID_VIEW_CUSTOMIZE, strCustomize);
		}
	}

	return TRUE;
}

BOOL MainFrame::OnCreateClient(LPCREATESTRUCT lpcs, CCreateContext* pContext)
{
	if (m_wndSplitter.CreateStatic(this, 1, 2, WS_CHILD | WS_VISIBLE) == FALSE)
	{
		TRACE(_T("FAILED when create static view"));
		return FALSE;
	}
	if (m_wndSplitter.CreateView(0, 0, RUNTIME_CLASS(FormViewBar), CSize(200, 500), pContext) == FALSE)
	{
		TRACE(_T("FAILED when create form view"));
		return FALSE;
	}
	if (m_wndSplitter.CreateView(0, 1, RUNTIME_CLASS(My3DView), CSize(500, 500), pContext) == FALSE)
	{
		TRACE(_T("FAILED when createor view"));
		return FALSE;
	};

	//formview_ = reinterpret_cast<FORMVIEW*>(m_wndSplitter.GetPane(0, 0));
	app_view = reinterpret_cast<My3DView*>(m_wndSplitter.GetPane(0, 1));

	return TRUE;
}
